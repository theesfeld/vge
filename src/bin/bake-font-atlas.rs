//! One-shot baker: B612 Mono TTF → `src/font_atlas_data.rs`
//!
//! ```text
//! cargo run --release --bin bake-font-atlas --features bake_font
//! ```
//!
//! Not part of the product binary set when `bake_font` is off.
//! Runtime library does not use fontdue.

use std::fs;
use std::path::PathBuf;

fn main() {
    let manifest = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    let ttf = manifest.join("assets/fonts/B612Mono-Regular.ttf");
    let out = manifest.join("src/font_atlas_data.rs");
    let bytes = fs::read(&ttf).expect("read B612Mono-Regular.ttf");
    let font = fontdue::Font::from_bytes(bytes.as_slice(), fontdue::FontSettings::default())
        .expect("parse B612");

    let sizes: &[u16] = &[12, 16, 20];
    let mut glyphs: Vec<String> = Vec::new();
    let mut coverage: Vec<u8> = Vec::new();
    let mut glyph_meta: Vec<String> = Vec::new();

    // Map: size_index * 96 + (ch - 0x20) for printables 0x20..=0x7E
    for &size in sizes {
        let px = size as f32;
        let line = font.horizontal_line_metrics(px).expect("line metrics");
        let ascent = line.ascent.round() as i16;
        let line_height = (line.ascent - line.descent + line.line_gap).round() as u16;

        for code in 0x20u8..=0x7Eu8 {
            let ch = code as char;
            let (m, bm) = font.rasterize(ch, px);
            let offset = coverage.len() as u32;
            coverage.extend_from_slice(&bm);
            // Pack: ch is implied by index; store metrics
            glyph_meta.push(format!(
                "    Glyph {{ w: {}, h: {}, xmin: {}, ymin: {}, advance: {}, off: {}, len: {} }}, // size={size} {ch:?}\n",
                m.width.min(255) as u8,
                m.height.min(255) as u8,
                m.xmin.clamp(-128, 127) as i8,
                m.ymin.clamp(-128, 127) as i8,
                m.advance_width.round().clamp(0.0, 255.0) as u8,
                offset,
                bm.len() as u32,
            ));
        }
        glyphs.push(format!(
            "    // size {size}: ascent={ascent} line_height={line_height}\n    Face {{ size: {size}, ascent: {ascent}, line_height: {line_height} }},\n"
        ));
    }

    let n_chars = (0x7E - 0x20 + 1) as usize;
    let mut file = String::new();
    file.push_str("//! AUTO-GENERATED by `bake-font-atlas` — do not edit by hand.\n");
    file.push_str("//! Source: assets/fonts/B612Mono-Regular.ttf (EPL-2.0).\n");
    file.push_str(
        "//! Regenerate: cargo run --release --bin bake-font-atlas --features bake_font\n\n",
    );
    file.push_str("#![allow(dead_code)]\n\n");
    file.push_str("/// One baked face size.\n");
    file.push_str("#[derive(Clone, Copy, Debug)]\n");
    file.push_str("pub struct Face {\n");
    file.push_str("    pub size: u16,\n");
    file.push_str("    pub ascent: i16,\n");
    file.push_str("    pub line_height: u16,\n");
    file.push_str("}\n\n");
    file.push_str("/// Glyph coverage blob metrics (8-bit coverage, row-major).\n");
    file.push_str("#[derive(Clone, Copy, Debug)]\n");
    file.push_str("pub struct Glyph {\n");
    file.push_str("    pub w: u8,\n");
    file.push_str("    pub h: u8,\n");
    file.push_str("    pub xmin: i8,\n");
    file.push_str("    pub ymin: i8,\n");
    file.push_str("    pub advance: u8,\n");
    file.push_str("    pub off: u32,\n");
    file.push_str("    pub len: u32,\n");
    file.push_str("}\n\n");
    file.push_str(&format!("pub const N_CHARS: usize = {n_chars};\n"));
    file.push_str("pub const FIRST_CHAR: u8 = 0x20;\n");
    file.push_str("pub const LAST_CHAR: u8 = 0x7E;\n\n");
    file.push_str("pub const FACES: &[Face] = &[\n");
    for g in &glyphs {
        file.push_str(g);
    }
    file.push_str("];\n\n");
    file.push_str("/// Glyphs packed: face_index * N_CHARS + (ch - FIRST_CHAR).\n");
    file.push_str("pub const GLYPHS: &[Glyph] = &[\n");
    for g in &glyph_meta {
        file.push_str(g);
    }
    file.push_str("];\n\n");
    file.push_str("pub const COVERAGE: &[u8] = &[\n");
    for (i, b) in coverage.iter().enumerate() {
        if i % 16 == 0 {
            file.push_str("    ");
        }
        file.push_str(&format!("{b}, "));
        if i % 16 == 15 {
            file.push('\n');
        }
    }
    if coverage.len() % 16 != 0 {
        file.push('\n');
    }
    file.push_str("];\n");

    fs::write(&out, file).expect("write font_atlas_data.rs");
    eprintln!(
        "wrote {} ({} coverage bytes, {} faces)",
        out.display(),
        coverage.len(),
        sizes.len()
    );
}
